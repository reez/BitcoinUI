import UIKit

extension CGFloat {
    
    static func pf_grid_10(_ n: Int) -> CGFloat {
        return CGFloat(n) * 10
    }
    
    static func pf_grid_vertical(_ n: Int) -> CGFloat {
        return CGFloat(n) * 20
    }
    
    static func pf_grid_horizontal(_ n: Int) -> CGFloat {
        return CGFloat(n) * 10
    }
    
}
